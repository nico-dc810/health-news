def write_json(path: Path, data) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def update_index() -> None:
    index_path = ROOT / "static" / "data" / "daily" / "index.json"
    index = json.loads(index_path.read_text(encoding="utf-8-sig"))
    index["items"] = [entry for entry in index.get("items", []) if entry.get("date") != DATE]
    tags = []
    for entry in items:
        tags.extend(entry["tags"][:2])
    index["items"].insert(
        0,
        {
            "date": DATE,
            "label": "2026/07/22",
            "title": TITLE,
            "count": len(items),
            "path": f"data/daily/{DATE}.json",
            "categories": sorted({entry["category"] for entry in items}),
            "top_tags": [tag for tag, _ in Counter(tags).most_common(12)],
        },
    )
    index["updated_at"] = UPDATED_AT
    write_json(index_path, index)


